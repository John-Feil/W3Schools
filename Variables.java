public class Variables {
    public static void main(String[] args) {
        String name = "John";
        System.out.println(name);
        int age = 20;
        System.out.println(age);
        int favNum;
        favNum = 9;
        System.out.println(favNum);
        int gradYear = 2000;
        gradYear = 2020;
        System.out.println(gradYear);
        final int birthday = 11302001; // cannot assign a new value to final variable
        float myFloatNum = 5.99f;
        char myLetter = 'J';
        boolean isItFriday = true;
        String lastName = "Feil";
        String fullName = name + lastName;
        System.out.println(fullName);
        int x = 9;
        int y = 7;
        System.out.println(5+6);
        int a = 1, b = 2, c =3;
        System.out.println(a + b + c);
    }
}
